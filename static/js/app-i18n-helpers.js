function _heightUnit(){ return PROFILE && PROFILE.units === 'imperial' ? 'ft/in' : 'cm'; }
function _weightUnit(){ return PROFILE && PROFILE.units === 'imperial' ? 'lb' : 'kg'; }
// Convert imperial input values → metric before PUT
function _waistToMetric(v){ return PROFILE && PROFILE.units === 'imperial' ? v * 2.54 : v; }
function _weightToMetric(v){ return PROFILE && PROFILE.units === 'imperial' ? v / 2.20462 : v; }
function _heightToMetric(v){ return PROFILE && PROFILE.units === 'imperial' ? v * 2.54 : v; } // used when single cm input
// ft/in string → cm (for imperial height input like "5'10")
function _ftInToCm(ftStr, inStr){
  var ft = parseFloat(ftStr)||0, inch = parseFloat(inStr)||0;
  return (ft * 12 + inch) * 2.54;
}

// Change locale + units via API and re-apply i18n
function setLocale(loc){
  fetch('/api/profile', {method:'PUT', headers:{'Content-Type':'application/json'}, body:JSON.stringify({locale:loc})})
    .then(function(r){return r.json();})
    .then(function(p){
      PROFILE = p;
      applyI18n();
      renderHoy();
      renderTend();
      renderCoach();
      if(coachRendered){ renderCoachTab(); }
      renderMasLocaleControls();
      // Fase 8B: labels de hábitos/impacto/informe vienen del backend
      // localizadas — re-fetch para que salgan en el idioma nuevo.
      if (typeof fetchJournalState === 'function') { fetchJournalState(); }
      if (typeof fetchAndRenderJournalImpact === 'function') { fetchAndRenderJournalImpact(); }
      if (typeof fetchAndRenderReportPreview === 'function') { fetchAndRenderReportPreview(); }
      if (typeof fetchAndRenderHealthspan === 'function') { fetchAndRenderHealthspan(); }
      // Roadmap P1 paso 1: Palancas (DRIVERS) es un snapshot embebido en el
      // load inicial que no se refrescaba al cambiar de idioma — re-fetch
      // para que los headlines salgan en el idioma nuevo (mismo patrón que
      // journal impact arriba).
      if (typeof fetchAndRenderDrivers === 'function') { fetchAndRenderDrivers(); }
    })
    .catch(function(){});
}
