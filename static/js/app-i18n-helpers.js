// ── Unit helpers ──
function fmtLen(cm){
  if(PROFILE && PROFILE.units === 'imperial'){
    return Math.round(cm / 2.54) + ' in';
  }
  return Math.round(cm) + ' cm';
}
function fmtWeight(kg){
  if(PROFILE && PROFILE.units === 'imperial'){
    return Math.round(kg * 2.20462) + ' lb';
  }
  return Math.round(kg) + ' kg';
}
function fmtHeight(cm){
  if(PROFILE && PROFILE.units === 'imperial'){
    var totalIn = cm / 2.54;
    var ft = Math.floor(totalIn / 12);
    var inch = Math.round(totalIn % 12);
    return ft + '\'' + inch + '"';
  }
  return Math.round(cm) + ' cm';
}
