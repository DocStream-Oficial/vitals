// ── Helpers de zona ACWR ──
function acwrZoneColor(zone){
  if(zone==='optimo')   return 'var(--green,#30D158)';
  if(zone==='precaucion') return 'var(--amber,#FF9F0A)';
  if(zone==='alto')     return 'var(--red,#FF375F)';
  return 'var(--label3)'; // detraining u otros
}
function acwrZoneLabel(zone){
  if(zone==='optimo')    return t('acwr_optimo');
  if(zone==='precaucion') return t('acwr_precaucion');
  if(zone==='alto')      return t('acwr_alto');
  if(zone==='detraining') return t('acwr_detraining');
  return zone || '—';
}

// ── Helper de badge de tendencia ──
function trendBadge(metric){
  var trend = TRENDS && TRENDS[metric];
  if(!trend) return '';
  var arrow = trend.direction==='subiendo' ? '↑' : (trend.direction==='bajando' ? '↓' : '→');
  var color = trend.significant
    ? (trend.direction==='subiendo' ? '#30D158' : (trend.direction==='bajando' ? '#FF375F' : '#8E8E93'))
    : '#8E8E93';
  var title = trend.significant ? t('trend_significant') : t('trend_not_significant');
  return '<span class="trend-badge" style="color:'+color+';font-size:13px;margin-left:5px" title="'+title+'">'+arrow+'</span>';
}

