// i18n lookup with ES fallback — never shows raw key. Comparación con
// undefined (no truthiness): '' es un valor legítimo (p. ej. today_prefix
// en EN/FR/PT) y con || caía de vuelta al español ("Hoy, 4 July").
function t(k){
  var loc = STRINGS[PROFILE && PROFILE.locale] || STRINGS.es;
  if (loc[k] !== undefined) return loc[k];
  if (STRINGS.es[k] !== undefined) return STRINGS.es[k];
  return k;
}

// Apply all data-i18n* attributes in the DOM
function applyI18n(){
  document.querySelectorAll('[data-i18n]').forEach(function(el){
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el){
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });
  document.querySelectorAll('[data-i18n-al]').forEach(function(el){
    el.setAttribute('aria-label', t(el.getAttribute('data-i18n-al')));
  });
  document.querySelectorAll('[data-i18n-t]').forEach(function(el){
    el.setAttribute('title', t(el.getAttribute('data-i18n-t')));
  });
  // Update order card names in ORDER_SCOPES with translated values.
  // ORDER_SCOPES.{hoy,tend}.names es un GETTER dinámico (definido más abajo,
  // ver "var ORDER_SCOPES") que ya recalcula t(...) en cada acceso — NO
  // reasignar aquí con un objeto plano, o se pierde el getter (y con él,
  // las cards nuevas de Fase 8B: journalCard/tendJournalImpactCard/
  // tendReportCard) en cada llamada a applyI18n().
}

