def tr(key: str, locale: str = "es", **kwargs) -> str:
    """Devuelve la traducción de la clave en el locale dado.

    Fallbacks:
    1. STRINGS[locale][key]
    2. STRINGS["es"][key]
    3. key cruda

    Luego aplica .format(**kwargs) si hay kwargs.
    KeyError en .format() → devuelve el string sin formatear (no crashea).
    """
    locale_dict = STRINGS.get(locale, STRINGS["es"])
    text = locale_dict.get(key)
    if text is None:
        text = STRINGS["es"].get(key, key)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text
